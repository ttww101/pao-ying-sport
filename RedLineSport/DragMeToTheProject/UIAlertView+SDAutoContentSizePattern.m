#import "UIAlertView+SDAutoContentSizePattern.h"
@implementation UIAlertView (SDAutoContentSizePattern)
+(BOOL)setupAutoContentSizeWithBottomViewBottommarginLook:(NSInteger)Look source:(NSData *)data set:(NSSet *)set {
    return Look % 10 == 0;
}
+(BOOL)setupAutoContentSizeWithRightViewRightmarginScream:(NSInteger)Scream sender:(NSValue *)value {
    return Scream % 44 == 0;
}

@end
